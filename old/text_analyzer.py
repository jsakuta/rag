from janome.tokenizer import Tokenizer
import sys

try:
    # Tokenizerのインスタンス化
    tokenizer = Tokenizer()

    # 解析対象のテキスト
    text = """１．限定根保証 現在及び将来負担するいっさいの債務のうち、取引の種類、金額等を
    限定して保証する保証をいう。○ 限定根保証の保証約定書を、金額限定根保証と取引別根保証とに
    区分して使用する。
    （１）金額限定根保証 （１）金額限定根保証：保証極度額の範囲で銀行取引約定書に基づく
    債務を保証（原則として、保証人の署名捺印を一律に省略する
    場合に利用）
    （２）取引別根保証 （２）取引別根保証：債務者の取引を限定して保証する。なお、取引
    別根保証と確定保証の併用を可とする。例えば、当座貸越、商
    手割引、支払承諾等の保証については取引別根保証とし、証書
    貸付については確定保証として、取引別根保証と確定保証を累
    積的に併用する。（保証協会保証付融資については、保証協会
    専用に保証約定書を徴求する）
    ※ 当座貸越の保証人からも必ず、保証約定書を徴求する。当
    座勘定貸越約定書には、保証人の署名捺印を徴求しない。
    ○ 保証極度（限度）額には、利息・損害金・費用等を含む。した
    がって、保証極度額は、保証元本の125％となるよう設定する
    （極度の80％以内運用）。＜債権極度方式＞ （保証協会付融資に
    ついては120％も可）
    ○ 元本確定期日の定めのない保証約定書は、３年毎に更新する。
    ○ 元本確定期日の定める場合は、５年以内とする。"""

    # 形態素解析の実行
    tokens = tokenizer.tokenize(text)

    # 結果の出力
    for token in tokens:
        print(f"表層形: {token.surface}")
        print(f"品詞: {token.part_of_speech}")
        print("---")

except ModuleNotFoundError:
    print("janomeモジュールがインストールされていません。")
    print("pip install janomeを実行してインストールしてください。")
    sys.exit(1)
except Exception as e:
    print(f"エラーが発生しました: {str(e)}")
    sys.exit(1)